#ifndef __DEBUG_H__
#define __DEBUG_H__

#define __DEBUG__	// デバッグ出力あり

/*============================================================================*/
/* Include */
/*============================================================================*/
#include <stdarg.h>

/*============================================================================*/
/* Macro */
/*============================================================================*/
#define DLEVEL	"DLEVEL"

#ifdef __DEBUG__

/*============================================================================*/
/* enum */
/*============================================================================*/
enum dlevel {
	DEBUG	= 5,
	INFO	= 4,
	WARN	= 3,
	ERROR	= 2,
	FATAL	= 1
};

/*============================================================================*/
/* extern(func) */
/*============================================================================*/
extern int _dprintf(enum dlevel dl, const char* file, int line, const char *fmt, ...);

/*============================================================================*/
/* Macro */
/*============================================================================*/
#define dprintf(dl, ...)	\
	_dprintf(dl, __FILE__, __LINE__, __VA_ARGS__)

#else // __DEBUG__

/*============================================================================*/
/* Macro */
/*============================================================================*/
#define dprintf(dl, ...)

#endif // __DEBUG__

#endif // __DEBUG_H__
